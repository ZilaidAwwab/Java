package com.zilaidawwab.javalang.basics.variables;

public class Variables {
    public static void main(String[] args) {

        // Declaring a variable of name num, type integer (int)
        int num;
        // Assigning a value to the variable
        num = 100;
        // Printing the variable
        System.out.println(num); // 100

        // Performing more operation on an already declared variable
        num = num * 2;
        System.out.println(num); // 200
    }
}
